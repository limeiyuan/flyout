public class OrderInfo {
    /**
     * 排序字段名
     */
    private String name;

    /**
     * 排序方式
     */
    private String sequence;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSequence() {
        return sequence;
    }

    public void setSequence(String sequence) {
        this.sequence = sequence;
    }
}
